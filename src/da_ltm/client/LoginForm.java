    private void connectToServer(){
        if(txtHost.getText().length() > 0 && txtPort.getText().length() > 0 && txtUsername.getText().length() > 0){
            if(txtUsername.getText().length() <= 15){
                String username = txtUsername.getText();
                String convertUsername = username.replace(" ", "-");
                /*  Hiện thị ClientChatFormWindow  */
                ClientChatForm main = new ClientChatForm();
                main.initFrame(convertUsername, txtHost.getText(), Integer.parseInt(txtPort.getText()));
                //  kiểm tra nếu như được kết nối
                if(main.isConnected()){
                    main.setLocationRelativeTo(null);
                    main.setVisible(true);
                    setVisible(false);
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "Dữ liệu nhập vào chưa đúng !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Dữ liệu nhập vào còn thiếu!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

